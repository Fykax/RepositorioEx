package jcolonia.daw2020.quinielabd;

public class TextoNuloException extends Exception {
	private static final long serialVersionUID = -550827255410197300L;

	public TextoNuloException() {
	}

	public TextoNuloException(String message) {
		super(message);
	}

	public TextoNuloException(Throwable cause) {
		super(cause);
	}

	public TextoNuloException(String message, Throwable cause) {
		super(message, cause);
	}

	public TextoNuloException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
